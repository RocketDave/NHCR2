create function import_facility()
returns void as 
$BODY$
begin
    insert into facility (
        facility_id,
        action_on,
        action_by,
        inserted_on,
        inserted_by,
        facility_name,
        address1,
        address2,
        address3,
        city,
        state,
        zip,
        fax,
        status,
        status_date,
        implementation_date,
        inspection_date,
        master_site,
        chase_trace,
        qa,
        ctc,
        how_write_reports,
        report_code,
        comments,
        contact_doctor_name,
        contact_doctor_phone,
        contact_doctor_email,
        contact_staff_name,
        contact_staff_phone,
        contact_staff_email,
        contact_computer_name,
        contact_computer_phone,
        contact_computer_email,
        contact_path_name,
        contact_path_phone,
        contact_path_email,
        contact_irb_name,
        contact_irb_phone,
        contact_irb_email,
        facility_type,
        contact_reports_name,
        contact_reports_phone,
        contact_reports_email,
        reports_notes,
        reports_instructions,
        pth_consent_form_reqd,
        pth_req_sort_col,
        pth_req_notes,
        pth_req_instructions,
        facility_pseudo_name,
        sort_pth_req_endo,
        pth_req_required
) 
    select
        facility_id,
        modify_date,
        modify_user,
        create_date,
        create_user,
        facility_name,
        address1,
        address2,
        address3,
        city,
        state,
        zip,
        fax,
        status,
        status_date,
        implementation_date,
        inspection_date,
        master_site,
        convert_true_false(chase_trace),
        convert_true_false(qa),
        convert_true_false(ctc),
        how_write_reports,
        report_code,
        comments,
        contact_doctor_name,
        contact_doctor_phone,
        contact_doctor_email,
        contact_staff_name,
        contact_staff_phone,
        contact_staff_email,
        contact_computer_name,
        contact_computer_phone,
        contact_computer_email,
        contact_path_name,
        contact_path_phone,
        contact_path_email,
        contact_irb_name,
        contact_irb_phone,
        contact_irb_email,
        facility_type,
        contact_reports_name,
        contact_reports_phone,
        contact_reports_email,
        reports_notes,
        reports_instructions,
        convert_true_false(pth_consent_form_reqd),
        pth_req_sort_col,
        pth_req_notes,
        pth_req_instructions,
        facility_pseudo_name,
        convert_true_false(sort_pth_req_endo),
        convert_true_false(pth_req_required)
    from 
        facility_import
    order by facility_id;
end;
$BODY$
language plpgsql;