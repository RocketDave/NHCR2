    inserted_by,
    path_report_id,
    path_polyp_loc,
    polyp_num,
    discrepnote,
    container,
    fragment,
    other_dx_specify,
    site_location_cm,
    site_desc,
    size_mm,
    anallevel,
    flg_no_path_spec,
    Ptype_Carcinoid,
