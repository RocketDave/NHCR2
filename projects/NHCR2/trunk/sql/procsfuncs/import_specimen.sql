    flg_size_discrep,
    SAS_key_id
    )
    select
    specimen_key_id,
    mod_rec_date,
    mod_rec_user,
    new_rec_date,
    new_rec_user,
    path_id,
    path_polyp_loc,
    polyp_num,
    discrepnote,
    container,
    fragment,
    other_dx_specify,
    site_location_cm,
    site_desc,
    size_mm,
    anallevel,
    convert_true_false(flg_no_path_spec),
    convert_true_false(Ptype_Carcinoid),
    convert_true_false(Ptype_Ganglio),
