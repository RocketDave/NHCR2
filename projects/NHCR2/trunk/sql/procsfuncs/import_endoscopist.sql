create function import_endoscopist()
returns void as 
$BODY$
begin

    update endoscopist_import set endo_dob = fix_dates_1899(endo_dob);
    update endoscopist_import set endo_status_date = fix_dates_1899(endo_status_date);

    insert into endoscopist (
        endoscopist_id,
        action_on,
        action_by,
        inserted_on,
        inserted_by,
        endo_initials,
        endo_first_name,
        endo_middle_name,
        endo_last_name,
        endo_name_suffix,
        endo_degree,
        salutation,
        mail_name,
        endo_organization,
        endo_address1,
        endo_address2,
        endo_address3,
        endo_city,
        endo_state,
        endo_zip,
        endo_direct_phone,
        endo_pager,
        endo_other_phone,
        endo_email,
        endo_reach_sun,
        endo_reach_mon,
        endo_reach_tues,
        endo_reach_wed,
        endo_reach_thurs,
        endo_reach_fri,
        endo_reach_sat,
        endo_dob,
        endo_gender_male,
        endo_specialty,
        endo_med_grad_year,
        endo_res_grad_year,
        endo_res_location,
        endo_fellowship,
        endo_fellow_grad_year,
        endo_fellow_location,
        endo_fellow_discipline,
        year_first_colo,
        year_first_colo_here,
        comments,
        report_group_code,
        report_group_lead,
        participating,
        main_site,
        report_handling,
        enroll_survey_done,
        steering_committee,
        endo_status,
        endo_status_date,
        endo_pseudo_name)
    select
        endo_code,
        modify_date,
        modify_user,
        create_date,
        create_user,
        endo_initials,
        endo_first_name,
        endo_middle_name,
        endo_last_name,
        endo_name_suffix,
        endo_degree,
        salutation,
        mail_name,
        endo_organization,
        endo_address1,
        endo_address2,
        endo_address3,
        endo_city,
        endo_state,
        endo_zip,
        endo_direct_phone,
        endo_pager,
        endo_other_phone,
        endo_email,
        convert_true_false(endo_reach_sun),
        convert_true_false(endo_reach_mon),
        convert_true_false(endo_reach_tues),
        convert_true_false(endo_reach_wed),
        convert_true_false(endo_reach_thurs),
        convert_true_false(endo_reach_fri),
        convert_true_false(endo_reach_sat),
        endo_dob,
        convert_true_false(endo_gender_male),
        endo_specialty,
        endo_med_grad_year,
        endo_res_grad_year,
        endo_res_location,
        convert_true_false(endo_fellowship),
        endo_fellow_grad_year,
        endo_fellow_location,
        endo_fellow_discipline,
        year_first_colo,
        year_first_colo_here,
        comments,
        report_group_code,
        convert_true_false(report_group_lead),
        convert_true_false(participating),
        main_site,
        report_handling,
        convert_true_false(enroll_survey_done),
        convert_true_false(steering_committee),
        endo_status,
        endo_status_date,
        endo_pseudo_name
    from 
        endoscopist_import;
end;
$BODY$
language plpgsql;